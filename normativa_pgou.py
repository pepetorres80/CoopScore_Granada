def buscar_chunks(query: str, n: int = 3) -> list[dict]:
    """Búsqueda por palabras clave sobre los chunks de normativa."""
    q = query.lower()
    palabras = [p for p in q.split() if len(p) > 3]

    puntuados = []
    for chunk in CHUNKS:
        texto = (chunk["titulo"] + " " + chunk["contenido"]).lower()
        score = sum(
            len([m for m in __import__("re").finditer(p, texto)])
            for p in palabras
        )
        puntuados.append((score, chunk))

    puntuados.sort(key=lambda x: -x[0])
    return [c for s, c in puntuados[:n] if s > 0]
